export const meta = {
  name: 'devils-advocate',
  description: '魔鬼代言人：以"摧毁这篇论文"为目标，从5个维度找出最致命的弱点，给出防御性修改方案',
  phases: [
    { title: '假设攻击', detail: '攻击每个模型假设的合理性' },
    { title: '方法攻击', detail: '质疑算法选择和创新声明' },
    { title: '结果攻击', detail: '质疑结果的可信度和因果性' },
    { title: '逻辑攻击', detail: '找出论证链条中的断点' },
    { title: '综合防御', detail: '汇总致命弱点并给出防御方案' },
  ],
}

// 魔鬼代言人工作流
// 目标：不是帮助论文，而是摧毁论文
// 只有找到最致命的弱点，才能真正加固论文
//
// 输入: args = { paperFile: "论文.tex", modelCode: "main.py" (可选) }
// 输出: 论文的致命弱点清单 + 防御性修改方案

const config = args || {}
const paperFile = config.paperFile || '论文.tex'
const modelCode = config.modelCode || 'main.py'
const problemType = config.problemType || '优化类'

log('========================================')
log('  魔鬼代言人 — 论文摧毁模式')
log('========================================')
log('目标：找出这篇论文如果被拒，原因会是什么')
log('')

// ===== 第一轮：五路并行攻击 =====
phase('假设攻击')
log('正在攻击模型假设...')

const [assumptionAttack, methodAttack, resultAttack, logicAttack, evidenceAttack] = await parallel([
  // 1. 假设攻击
  () => agent(
    `你是数学建模竞赛的"魔鬼代言人"。你的任务不是帮助这篇论文，而是**摧毁它**。

论文文件: ${paperFile}
问题类型: ${problemType}

## 攻击维度1：模型假设

逐一审查每条模型假设，问：
1. **这个假设真的合理吗？** 找出每个假设的反例或边界情况
2. **如果这个假设不成立，会发生什么？** 会推翻整个模型吗？还是只影响精度？
3. **这些假设之间有矛盾吗？** 比如"假设速度恒定"和"考虑时变交通"能否共存？
4. **假设是否过于简化？** 有没有关键的物理/经济因素被忽略了？
5. **假设的"合理说明"是否只是循环论证？** "假设速度恒定以简化模型"——这不是理由，只是重申假设

## 攻击输出格式
对每条假设：
### 假设N: [原文]
**攻击**：[具体攻击论点，引用反例/数据/逻辑矛盾]
**致命程度**：致命 / 严重 / 一般 / 轻微
**如果我是评委，我会写**："这条假设的问题在于..."

### 假设攻击总结
- 致命假设数：X
- 最致命的假设：[序号] — [一句话说明为什么]
- 如果这条假设被攻破，论文的结论：[完全崩溃 / 部分受影响 / 基本不受影响]`,
    { label: '攻击假设', phase: '假设攻击' }
  ),

  // 2. 方法攻击
  () => agent(
    `你是数学建模竞赛的"魔鬼代言人"。你的任务是**摧毁这篇论文的方法论基础**。

论文文件: ${paperFile}
模型代码（如有）: ${modelCode}
问题类型: ${problemType}

## 攻击维度2：方法与算法

逐一审查方法选择和创新声明，问：
1. **为什么选这个算法而不是更简单的方法？** 如果简单方法就能得到类似结果，那复杂方法就是过度设计
2. **"创新"是真的创新吗？** 对照标准算法库，检查是否只是换了个名字的标准方法
3. **参数是怎么选的？** "经过实验调优"=没有依据。"参考文献推荐"=可能有不同的问题设置
4. **算法的计算复杂度分析正确吗？** 声称O(n²)但实际上有隐式O(n³)操作？
5. **收敛性有保证吗？** 启发式算法可能收敛到很差的局部最优

## 攻击输出格式
对每个方法声明：
### 声明N: [论文原文]
**攻击**：[具体攻击论点]
**致命程度**：致命 / 严重 / 一般 / 轻微
**标准答案**：这个问题类型的标准做法是...（如果论文偏离了标准做法，要么是创新，要么是错误）

### 方法攻击总结
- 最可疑的创新声明：
- 最弱的参数选择：
- 如果换一个更简单的算法，是否可能得到相同结果？`,
    { label: '攻击方法', phase: '方法攻击' }
  ),

  // 3. 结果攻击
  () => agent(
    `你是数学建模竞赛的"魔鬼代言人"。你的任务是**质疑所有结果的真实性**。

论文文件: ${paperFile}
问题类型: ${problemType}

## 攻击维度3：结果与数据

逐一审查结果的可靠性，问：
1. **这个结果是模型能力还是运气？** 单次运行结果不能说明任何问题
2. **训练集和测试集的划分公平吗？** 有没有数据泄露（如用未来信息预测过去）？
3. **"显著优于"有统计依据吗？** 提升2%但没有做统计检验 → 可能是噪声
4. **结果是否有选择偏误？** 是不是只报告了最好的那次运行？
5. **图表是否误导？** Y轴是否从0开始？比例尺是否被操纵？
6. **异常值/离群点是否被悄悄删除了？** 如果删了，论文有没有说明？

## 攻击输出格式
对每个结果声明：
### 结果N: [论文原文]
**攻击**：[为什么这个数字可能不可信]
**检验方法**：[如何验证这个结果是否真实——如需要多少次重复实验、什么统计检验]
**致命程度**：致命 / 严重 / 一般 / 轻微

### 结果攻击总结
- 最不可信的结果：
- 缺失的关键检验：
- 如果重新运行，这个结果再次出现的概率：`,
    { label: '攻击结果', phase: '结果攻击' }
  ),

  // 4. 逻辑攻击
  () => agent(
    `你是数学建模竞赛的"魔鬼代言人"。你的任务是**找出论证链条中的每一个断裂点**。

论文文件: ${paperFile}
问题类型: ${problemType}

## 攻击维度4：逻辑与论证

逐一审查论证的逻辑完整性，问：
1. **"因为A所以B"的因果关系真的成立吗？** 是因果还是只是相关？
2. **结论是否超出了模型能证明的范围？** "本模型可以应用于..."真的可以吗？
3. **摘要的承诺在正文中兑现了吗？** 摘要说"大幅提升"，正文没有对比数据 → 诈骗
4. **有没有"然后奇迹发生了"的跳步？** 从公式到结果中间的关键推理步骤缺失
5. **前后矛盾吗？** 第三章说假设X简化了问题，第六章说模型考虑了X的影响

## 攻击输出格式
对每个逻辑断点：
### 断点N: [位置]
**声称**：[论文说了什么]
**问题**：[为什么这个推理不成立]
**缺失的论证**：[需要什么才能使这个推理成立]
**致命程度**：致命 / 严重 / 一般 / 轻微

### 逻辑攻击总结
- 最明显的逻辑跳跃：
- 如果我是评委，最不能接受的一个推理：
- 论证链条中最薄弱的一环：`,
    { label: '攻击逻辑', phase: '逻辑攻击' }
  ),

  // 5. 证据攻击
  () => agent(
    `你是数学建模竞赛的"魔鬼代言人"。你的任务是**找出所有没有数据支撑的断言**。

论文文件: ${paperFile}
问题类型: ${problemType}

## 攻击维度5：证据与引用

逐一审查论文中的断言是否有充分支撑，问：
1. **"具有较好的鲁棒性/精度/效率"——数字呢？** 没有定量证据的定性断言 = 空话
2. **"与现有方法相比..."——引用了哪些现有方法？** 没有引用 = 没有对比
3. **"可以推广到..."——真的验证过吗？** 没有在新场景测试的推广声明 = 空想
4. **参考文献真的支持论文的声明吗？** 是不是随便引了几篇无关的？
5. **管理启示有数据支撑吗？** "建议企业优化配送路线"——优化后具体能省多少？计算过程？

## 攻击输出格式
对每个无支撑断言：
### 断言N: [论文原文，位置]
**为什么是空的**：[缺少什么证据]
**如何补证据**：[最简单的证明方式]
**致命程度**：致命 / 严重 / 一般 / 轻微

### 证据攻击总结
- 空洞断言数量：XX处
- 最严重的无支撑声明：
- 如果删掉所有无支撑断言，论文还剩多少实质内容：`,
    { label: '攻击证据', phase: '逻辑攻击' }
  ),
])

// ===== 第二轮：综合防御方案 =====
phase('综合防御')
log('正在综合5路攻击结果，生成防御方案...')

const defensePlan = await agent(
  `你是论文的"辩护律师"。魔鬼代言人已经从5个维度发起了攻击。现在请你：

1. 评估每路攻击的有效性（承认有道理的，反驳不成立的）
2. 对真正致命的问题，给出防御性修改方案
3. 区分"必须修"和"可以辩解"的问题

## 假设攻击
${assumptionAttack}

## 方法攻击
${methodAttack}

## 结果攻击
${resultAttack}

## 逻辑攻击
${logicAttack}

## 证据攻击
${evidenceAttack}

## 输出格式

# 魔鬼代言人攻击评估报告

## 一、致命弱点（必须修改，否则大概率被拒）
| 排名 | 攻击来源 | 弱点描述 | 致命程度 | 防御方案 | 预计修改时间 |
|------|---------|---------|---------|---------|------------|
| 1 | [假设/方法/结果/逻辑/证据] | ... | 致命 | ... | X分钟 |
| 2 | ... | ... | ... | ... | ... |

## 二、严重问题（强烈建议修改）
| 排名 | 弱点描述 | 防御方案 | 预计修改时间 |
|------|---------|---------|------------|
| 1 | ... | ... | ... |

## 三、可以辩解的问题（准备好在答辩中回应，但不一定需要修改论文）
| 问题 | 攻击来源 | 辩解策略 |
|------|---------|---------|
| ... | ... | ... |

## 四、攻击有效性评估
- 假设攻击：XX%有效（承认X条，驳回X条）
- 方法攻击：XX%有效
- 结果攻击：XX%有效
- 逻辑攻击：XX%有效
- 证据攻击：XX%有效

## 五、防御后的预估得分变化
- 防御前（攻击生效）：估计扣XX分
- 防御后（修复致命+严重问题）：估计只扣XX分
- **净收益：挽回XX分**

## 六、如果只修一处
最该修的是：[具体问题] — 因为修复这个会同时消除N个衍生问题`,
  { label: '综合防御方案', phase: '综合防御' }
)

log('')
log('========================================')
log('  魔鬼代言人攻击完成')
log('========================================')
log(`致命弱点: ${defensePlan.includes('致命') ? '存在——必须修复' : '未发现致命弱点'}`)
log('')

return {
  attacks: {
    assumption: assumptionAttack,
    method: methodAttack,
    result: resultAttack,
    logic: logicAttack,
    evidence: evidenceAttack,
  },
  defensePlan,
}
