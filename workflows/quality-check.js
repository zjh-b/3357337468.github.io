export const meta = {
  name: 'quality-check',
  description: '论文质量审查：自动检查DOCX/LaTeX论文的图表嵌入、公式完整性、参考文献、字体颜色等',
  phases: [
    { title: '格式审查', detail: '检查字体颜色、字号、页边距等格式规范' },
    { title: '内容审查', detail: '检查图表、公式、参考文献完整性' },
    { title: '输出报告', detail: '生成质量审查报告和修复建议' },
  ],
}

// 论文质量审查工作流
// 输入: args = { paperFile: "论文.tex" or "论文.docx", type: "latex" or "docx" }
// 输出: 质量审查报告

const config = args || {}
const paperFile = config.paperFile || '论文.docx'
const paperType = config.type || (paperFile.endsWith('.tex') ? 'latex' : 'docx')

phase('格式审查')
log(`正在审查 ${paperFile} 的格式规范...`)

const formatCheck = await agent(
  `请审查论文文件"${paperFile}"（${paperType}格式）的格式规范：

## 格式审查清单

### A. 字体与颜色
- [ ] 所有正文是否为黑色字体？（不应出现蓝色等非黑色文字）
- [ ] 标题是否都使用黑体？
- [ ] 正文是否使用宋体12pt？
- [ ] 一级标题是否14pt居中？
- [ ] 二级标题是否12pt左对齐？

### B. 段落格式
- [ ] 正文首行是否缩进2字符？
- [ ] 行距是否约1.3倍？
- [ ] 段落间是否有适当间距？

### C. 公式格式（LaTeX）
- [ ] 是否使用 equation/align 环境？
- [ ] 是否禁用 \\$\\$...\\$\\$？
- [ ] 每个公式是否有 \\label？
- [ ] 上下标是否正确（x_k 不是 xk，R^2 不是 R2）？

### D. 公式格式（DOCX）
- [ ] 公式中的 _ 下标是否正确渲染为 subscript？
- [ ] 公式中的 ^ 上标是否正确渲染为 superscript？
- [ ] Greek字母是否正确转换为对应符号？

### E. 页面设置
- [ ] 纸张大小是否正确（A4或US Letter）？
- [ ] 页边距是否符合规范？

对每项输出 ✅ 或 ❌（附具体位置和修复建议）。`,
  { label: '格式审查', phase: '格式审查' }
)

phase('内容审查')
log('正在审查内容完整性...')

const contentCheck = await agent(
  `请审查论文文件"${paperFile}"的内容完整性：

## 内容审查清单

### A. 图表审查
- [ ] 文中引用的图表是否都有对应的实际图片嵌入？
- [ ] 图片是否清晰可读（分辨率≥150dpi）？
- [ ] 每个图表是否有编号和标题？
- [ ] 图表标题是否在正确位置（表在上、图在下）？
- [ ] 正文中是否引用了所有图表？

### B. 公式审查
- [ ] 所有公式是否完整呈现（无截断）？
- [ ] 关键公式是否存在：
  - 卡尔曼滤波状态/观测方程
  - RMSE和R²定义
  - PELT目标函数
  - 互信息 I(X;Y) 完整表达式
  - 基尼重要性完整表达式
  - GBDT递推公式
  - Isolation Forest异常分数
- [ ] 每个公式前后是否有文字解释？

### C. 参考文献审查
- [ ] 参考文献是否在正文中被引用？
- [ ] 正文中的引用是否在参考文献列表中？
- [ ] 参考文献数量是否≥10篇？
- [ ] 是否包含中英文混合文献？
- [ ] 参考文献格式是否规范？

### D. 结构完整性
- [ ] 是否有封面（竞赛名称+题号+标题）？
- [ ] 是否有摘要（300-500字）和关键词？
- [ ] 8个章节是否完整（一至八+参考文献+附录）？
- [ ] 各章节篇幅是否合理（核心模型章节>50%）？
- [ ] 是否有目录？

对每项输出 ✅ 或 ❌（附缺失内容和修复建议）。`,
  { label: '内容审查', phase: '内容审查' }
)

phase('输出报告')
log('正在生成质量审查报告...')

const finalReport = await agent(
  `请综合以下两份审查结果，生成最终质量审查报告：

## 格式审查
${formatCheck}

## 内容审查
${contentCheck}

## 输出格式

# 论文质量审查报告

## 一、总体评分（满分100分）
- 格式规范：XX/40
- 内容完整性：XX/40
- 图表公式质量：XX/20
- **总分：XX/100**

## 二、格式问题（按严重程度排序）
| # | 严重程度 | 问题描述 | 位置 | 修复建议 |
|---|---------|---------|------|---------|
| 1 | 严重/一般/轻微 | ... | ... | ... |

## 三、内容缺失
列出所有缺失的图表、公式、参考文献

## 四、修复优先级
1. 🔴 必须修复（影响论文评审）：...
2. 🟡 建议修复（提升论文质量）：...
3. 🟢 可选优化（锦上添花）：...

## 五、一键修复建议
给出最高效的修复步骤`,
  { label: '生成审查报告', phase: '输出报告' }
)

log('质量审查完成！')
return { formatCheck, contentCheck, finalReport }
