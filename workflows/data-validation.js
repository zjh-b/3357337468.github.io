export const meta = {
  name: 'data-validation',
  description: '数据验证：检测模型输出是否符合物理规律、数学逻辑、统计显著性和实际约束',
  phases: [
    { title: '物理验证', detail: '检查物理规律一致性' },
    { title: '数学验证', detail: '检查数学逻辑和约束' },
    { title: '统计验证', detail: '统计显著性、过拟合检测、残差分析' },
    { title: '鲁棒性测试', detail: '灵敏度分析和极端情况测试' },
    { title: '综合报告', detail: '生成验证报告' },
  ],
}

const context = args || '模型输出文件'

phase('物理验证')
log('正在验证物理规律一致性...')

const physicsCheck = await agent(
  `请对数学建模的输出结果进行物理规律验证。检查以下内容：

${context}

## 物理规律检查清单

### 1. 量纲一致性
- 所有计算结果是否量纲正确？（如距离单位是米还是千米）
- 加减运算中各项量纲是否一致？
- 等式两边量纲是否匹配？

### 2. 守恒定律
- 质量/数量守恒：输入总量是否等于输出总量？（如车辆数、货物量）
- 能量守恒：能量输入输出是否平衡？（如有能源相关计算）
- 流量守恒：网络流中流入是否等于流出？

### 3. 边界条件
- 所有变量是否在合理的物理范围内？
  - 速度不能为负
  - 距离不能为负
  - 概率在[0,1]之间
  - 时间非负且不超过合理上限
  - 载重量不超过车辆额定载荷
  - 电池电量在[0,100%]之间

### 4. 逻辑约束
- 时间先后顺序是否合理？（到达时间 < 离开时间等）
- 空间位置是否合理？（坐标是否在服务区域内）
- 容量限制是否违反？

### 5. 统计合理性
- 均值、方差等统计量是否在合理范围？
- 是否有异常突兀的数据点？
- 分布是否符合预期形态？

请逐一检查，列出所有发现的问题，区分"严重违规"和"需要注意"。`,
  { label: '物理规律验证', phase: '物理验证' }
)

phase('数学验证')
log('正在验证数学逻辑和约束条件...')

const mathCheck = await agent(
  `请对模型输出进行数学逻辑验证：

${context}

## 数学验证清单

### 1. 目标函数值验证
- 目标函数值是否与各组成部分之和一致？
- 分别计算各部分成本/收益，验证总和正确

### 2. 约束满足度检查
- 每条约束是否严格满足？
- 如有软约束，违反程度是否在可接受范围？
- 列出所有约束违反情况（包括轻微违反）

### 3. KKT条件检查（如有连续优化）
- 一阶必要条件是否近似满足？
- 互补松弛条件是否满足？

### 4. 对偶验证
- 如果问题有对偶形式，原问题和对偶问题的目标值是否满足弱对偶？

### 5. 数值精度检查
- 是否有大数吃小数的问题？
- 数值误差是否在可接受范围（相对误差 < 1e-6）？

请给出每个检查项的结果：✅通过 / ⚠️警告 / ❌失败。`,
  { label: '数学逻辑验证', phase: '数学验证' }
)

phase('统计验证')
log('正在进行统计显著性检验、过拟合检测和残差分析...')

const statsCheck = await agent(
  `请对模型输出进行统计验证：

${context}

## 统计验证清单

### 1. 统计显著性检验
- 如果涉及模型对比（如"本文方法 vs 基线方法"），进行配对t检验或Wilcoxon检验
- 报告p值，判断差异是否统计显著（p < 0.05）
- 如果p > 0.05，标注"差异不显著——不建议声称本文方法优于基线"
- 计算效应量（Cohen's d），判断差异的实际意义

### 2. 过拟合检测（预测/拟合类问题必做）
- 比较训练集、验证集、测试集上的性能指标
- 如果训练集R² >> 测试集R²（差距>0.15），标注"疑似过拟合"
- 检查模型参数数量 vs 样本量（参数/样本比 > 1/10 时警告）
- 建议：交叉验证结果、正则化效果、学习曲线分析

### 3. 残差分析（预测/拟合类问题必做）
- 绘制残差 vs 拟合值图（检查异方差性）
- 绘制残差 QQ 图（检查正态性）
- 检查残差自相关（Durbin-Watson统计量）
- 如果残差呈现明显模式（如U形、漏斗形），标注"模型遗漏了关键变量或非线性关系"
- 分析最大残差点：这些异常点是否有合理的物理解释？

### 4. 多重共线性检查（回归类问题）
- 计算VIF（方差膨胀因子）
- VIF > 10 的变量标注为"存在严重共线性，模型系数不可靠"

### 5. 预测区间/置信区间
- 不仅给出点预测，还应给出预测区间（如95%CI）
- 检查真实值落在预测区间内的比例是否接近95%

请给出每个检查项的结果：✅通过 / ⚠️警告 / ❌失败，并附具体数值和诊断建议。`,
  { label: '统计验证', phase: '统计验证' }
)

phase('鲁棒性测试')
log('正在进行灵敏度分析和极端情况测试...')

const robustnessCheck = await agent(
  `请设计并执行模型的灵敏度分析和鲁棒性测试：

${context}

## 鲁棒性测试清单

### 1. 参数灵敏度分析（深度要求提升）
- 识别模型中的关键参数
- 对每个关键参数，在±10%、±20%、±50%范围内变化
- **不只是报告"影响不大"，而要挖掘非平凡关系**：
  - 是否存在阈值效应？（参数超过某值后结果突变）
  - 是否存在非单调关系？（参数增大，结果先升后降）
  - 哪些参数之间有交互效应？（A的变化放大/削弱了B的影响）
- 生成灵敏度分析热力图或龙卷风图
- 对每个非平凡发现，给出管理/工程解释

### 2. 极端情况测试
- 最小规模测试（如只有1个客户/1辆车）
- 最大规模测试（所有参数取最大值）
- 零值测试（某些参数为零的情况）
- 对称性测试（所有数据相同的情况）
- 边界测试（参数取定义域边界值）

### 3. 随机扰动测试（蒙特卡洛）
- 对输入数据添加随机噪声（如±1%、±5%、±10%）
- 每组噪声水平重复至少100次
- 统计结果的分布特征（均值、标准差、变异系数CV）
- CV > 15% 的参数标注为"模型对该参数高度敏感"

### 4. 对抗性测试（找模型的"软肋"）
- 构造最坏情况输入（使模型表现最差的数据组合）
- 分析模型在什么条件下会失效
- 给出模型的"安全使用边界"

请输出详细的鲁棒性测试报告。`,
  { label: '鲁棒性测试', phase: '鲁棒性测试' }
)

phase('综合报告')
log('正在生成验证综合报告...')

const validationReport = await agent(
  `请整合以下四份检查报告，生成最终的验证综合报告：

## 物理规律验证
${physicsCheck}

## 数学逻辑验证
${mathCheck}

## 统计验证
${statsCheck}

## 鲁棒性测试
${robustnessCheck}

请输出：

# 模型验证综合报告

## 一、总体评估
（✅ 通过 / ⚠️ 条件通过 / ❌ 不通过）

## 二、问题汇总表
（以表格形式列出所有发现的问题，按严重程度排序）

## 三、关键问题详细分析
（对严重和中等问题逐一分析原因和影响）

## 四、修复建议
（针对每个问题给出具体的修复方案）

## 五、模型可信度评估
（基于验证结果，评估模型的可信度和适用范围）

## 六、使用限制说明
（明确指出模型在什么条件下有效、什么条件下可能失效）`,
  { label: '生成验证报告', phase: '综合报告' }
)

log('数据验证完成！')
return { physicsCheck, mathCheck, statsCheck, robustnessCheck, validationReport }
