// 自我优化工作流
// 目标：让工作流系统从每一次建模中学习和进化
//
// 核心机制：
//   1. 赛后复盘 —— 分析本次建模全过程，找出可改进之处
//   2. 教训提取 —— 将具体经验转化为可复用的通用规则
//   3. Prompt进化 —— 基于实际产出质量，优化agent prompt
//   4. 评分校准 —— 对比模拟评分 vs 实际评分，校准预测模型
//   5. 知识沉淀 —— 输出结构化的记忆文件，供下次建模时注入
//
// 输入: args = {
//   paperFile: "论文.tex",              // 最终论文
//   judgeReport: "评委模拟的完整输出",    // 评委模拟的结果
//   devilReport: "魔鬼代言人的输出",      // 魔鬼代言人的发现
//   actualScore: 85,                    // 实际得分（如果已知）
//   actualPrize: "省一",                // 实际获奖（如果已知）
//   teamNotes: "队友的赛后总结",         // 人工反馈
//   previousLessons: "之前的教训(如果有)"  // 历史记忆
// }
// 输出: 结构化的优化建议 + 可保存的记忆文件

const config = args || {}
const paperFile = config.paperFile || '论文.tex'
const judgeReport = config.judgeReport || ''
const devilReport = config.devilReport || ''
const actualScore = config.actualScore || null
const actualPrize = config.actualPrize || null
const teamNotes = config.teamNotes || ''
const previousLessons = config.previousLessons || '无（这是第一次运行）'

log('========================================')
log('  工作流自我进化系统')
log('========================================')
log('从每一次建模中学习，让下一次更强大')
log('')

// ===== 第一阶段：赛后复盘 =====
phase('赛后复盘')
log('正在复盘本次建模全过程...')

const postMortem = await agent(
  `请对本次数学建模竞赛进行全面的赛后复盘。

## 历史教训（如果有）
${previousLessons}

## 最终论文
（请读取 ${paperFile} 进行分析）

## 评委模拟结果
${judgeReport}

## 魔鬼代言人攻击发现
${devilReport}

## 实际结果（如果已知）
实际得分: ${actualScore || '未知'}
实际获奖: ${actualPrize || '未知'}

## 队友反馈
${teamNotes || '无'}

## 复盘维度

### 1. 时间线复盘
- 每个阶段实际耗时 vs 计划耗时？
- 哪个阶段超出预期最多？为什么？
- 哪个阶段比预期快？快在哪里？

### 2. 技术复盘
- 模型选择是否正确？如果用备选方案会怎样？
- 基线对比是否发挥了作用？（是否帮助发现了模型的真实提升幅度？）
- 质量决策是否正确？（"通过"的决策事后看是否该"不通过"？反之亦然？）

### 3. 论文复盘
- 叙事主线是否在最终论文中得到贯彻？
- 评委模拟预估的分数 vs 实际分数（如果已知）差距多大？原因是？
- 魔鬼代言人发现的致命弱点中，哪些真的出现了？哪些是误报？
- 管理启示子节(6.5)是否真的为论文增值了？

### 4. 工作流复盘
- 哪些工作流阶段产出质量高？哪些低？
- 哪些agent prompt效果特别好/特别差？（引用具体输出）
- 并行vs串行的安排是否合理？有没有因为并行导致的质量损失？

### 5. 最关键的复盘问题
如果重新来一次，我会在哪些节点做出不同的决定？

## 输出格式
# 赛后复盘报告

## 时间线分析
（表格：计划vs实际耗时）

## 关键决策回顾
（每个重大决策：当时怎么选的 → 事后看是否正确 → 学到了什么）

## 工作流质量评估
（每个阶段：输出质量评级 A/B/C/D + 具体问题 + 改进方向）

## 如果重来一次
（按优先级排序的5个改变）`,
  { label: '全面复盘', phase: '赛后复盘' }
)

// ===== 第二阶段：教训提取 =====
phase('教训提取')
log('正在从复盘结果中提取可复用的教训...')

const lessonsExtracted = await agent(
  `请从以下复盘报告中提取**可复用的通用教训**。注意：不是"这次哪里做错了"，而是"下次应该怎么做"。

## 复盘报告
${postMortem}

## 教训提取要求

### 教训必须满足的标准
1. **可操作**：不是"要更仔细"而是"在数据预处理后，追加一个'数据合理性检查'步骤"
2. **可复用**：不是针对这个题目的，而是针对这类题目的
3. **可验证**：下次建模时可以检查是否执行了

### 教训分类

#### A类：流程教训（影响工作流结构的）
例："在算法选型后必须先跑基线，否则永远不知道复杂模型是否真的有必要"

#### B类：Prompt教训（影响agent prompt写法的）
例："让agent'检查异常值'的效果不好——改为'列出所有超过3σ的数据点并标注可能原因'后效果显著提升"

#### C类：格式教训（影响论文模板/规范的）
例："美赛评委更看重intuition和clarity而非公式数量——公式密度比国赛低30%更合适"

#### D类：问题类型特定教训（针对某类问题的）
例："预测类问题中，如果MAPE不合适（数据含零点），必须在论文中主动说明为什么改用RMSE"

#### E类：评委偏好教训（来自实际评分反馈）
例："评委对'管理启示'子节的评分权重远超预期——建议从200字扩展到400字"

### 输出格式
# 提取的教训（可复用）

## A类：流程教训
| # | 教训 | 触发条件 | 具体操作 | 来源（哪次比赛/哪个题目） |
|---|------|---------|---------|------------------------|
| 1 | ... | 当...时 | 应该... | 2026年华中杯C题 |

## B类：Prompt教训
| # | 原始Prompt的问题 | 改进后的Prompt | 改进效果 |
|---|----------------|---------------|---------|
| 1 | ... | ... | ... |

## C/D/E类同理...

## 最大的3个教训（如果只能记住3个）
1. ...
2. ...
3. ...`,
  { label: '提取可复用教训', phase: '教训提取' }
)

// ===== 第三阶段：Prompt进化 =====
phase('Prompt进化')
log('正在基于实际产出质量优化agent prompt...')

const promptEvolution = await agent(
  `请基于复盘报告和教训，对工作流中的agent prompt进行**具体优化**。

## 复盘报告
${postMortem.substring(0, 3000)}

## 提取的教训
${lessonsExtracted.substring(0, 3000)}

## 当前工作流中需要优化的prompt

请针对以下工作流的具体prompt，给出优化版本：

### 1. paper-generate.js — "确定叙事主线"阶段的prompt
当前问题：（从复盘报告中提取）
优化方向：如何让叙事主线更具体、更有记忆点？

### 2. paper-generate.js — "五、模型的建立与求解"agent的prompt
当前问题：三层公式解释是否被agent正确执行了？
优化方向：如何让agent更可靠地产出三层解释？

### 3. paper-polish.js — "废话检测"阶段的prompt
当前问题：是否漏检了某些废话模式？
优化方向：补充什么检测规则？

### 4. data-validation.js — "统计验证"阶段的prompt
当前问题：统计检验是否被正确应用？
优化方向：如何根据问题类型自动选择正确的检验方法？

### 5. judge-simulation.js — "快速扫描"阶段的prompt
当前问题：模拟评委的评分与实际评分偏差多大？
优化方向：如何让模拟更接近真实评委？

## 输出格式
对每个prompt：
### 原始Prompt的问题
### 优化后的Prompt（完整文本，可直接替换）
### 优化理由
### 预期改进效果`,
  { label: 'Prompt进化', phase: 'Prompt进化' }
)

// ===== 第四阶段：评分校准 =====
phase('评分校准')
log('正在校准评委模拟的评分准确性...')

const scoreCalibration = await agent(
  `请分析评委模拟评分与实际评分之间的偏差，并校准评分模型。

## 评委模拟评分
${judgeReport}

## 实际结果
实际得分: ${actualScore || '未知'}
实际获奖: ${actualPrize || '未知'}

## 魔鬼代言人发现的问题中，有多少被实际评委指出了？
${devilReport}

${!actualScore ? `
⚠️ 实际得分未知，本次校准基于以下假设进行：
- 比较魔鬼代言人的攻击点和评委模拟的扣分点
- 检查是否存在"评委模拟认为没问题但魔鬼代言人认为致命"的分歧
- 这种分歧 = 评委模拟的盲区
` : ''}

## 校准分析要求

### 1. 评分偏差分析（如果已知实际分数）
- 评委模拟预估: XX分 → 实际: XX分 → 偏差: ±XX分
- 哪个维度偏差最大？（建模/算法/结果/写作/创新）
- 偏差是系统性高估还是低估？

### 2. 盲区识别
- 魔鬼代言人发现但评委模拟未发现的问题 = 评委模拟的"盲区"
- 盲区主要集中在哪个维度？
- 如何扩展评委模拟以覆盖这些盲区？

### 3. 校准建议
- 评委模拟的评分应该整体上调/下调X分？
- 哪些维度的权重应该调整？
- 魔鬼代言人的发现应该以什么权重纳入评委模拟？

### 输出格式
# 评分校准报告

## 偏差分析
| 维度 | 模拟分 | 实际分 | 偏差 | 方向 |
|------|--------|--------|------|------|
| ... | ... | ... | ... | 高估/低估 |

## 盲区清单
| 盲区 | 为什么模拟评委没发现 | 魔鬼代言人如何发现的 | 如何改进 |
|------|-------------------|-------------------|---------|
| ... | ... | ... | ... |

## 校准参数（供下次评委模拟使用）
- 整体校准系数: X.XX
- 各维度权重调整: ...`,
  { label: '评分校准', phase: '评分校准' }
)

// ===== 第五阶段：知识更新 =====
