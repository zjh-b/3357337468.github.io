export const meta = {
  name: 'problem-analysis',
  description: '数学建模题目深度分析：解析问题背景、识别关键约束、梳理解题思路',
  phases: [
    { title: '阅读题目', detail: '读入并理解题目文本' },
    { title: '背景调研', detail: '搜索问题相关的背景知识' },
    { title: '综合分析', detail: '输出结构化题目分析报告' },
  ],
}

// 题目分析工作流
// 输入: 题目文件路径（通过 args 传入）
// 输出: 结构化题目分析报告

const problemFile = args || '题目.txt'

// 项目内范文资源
const REFERENCE_PAPERS = '2020国赛优秀论文/ 目录下有17篇一等奖论文（A-E题全覆盖），可作为解题思路和写作风格的参考。A题为工程/物理类，B题为优化/决策类，C题为数据分析/预测类，D题为评估类，E题为综合类。算法集锦/ 目录下有4份算法匹配指南。'

phase('阅读题目')
log(`正在分析题目: ${problemFile}`)
log('项目内有17篇国赛一等奖范文可供参考')

// 第一步：仔细阅读题目，提取关键信息
const problemInfo = await agent(
  `请仔细阅读题目文件"${problemFile}"，提取以下信息并以结构化形式输出：

1. **题目背景**：问题来自什么实际场景？有什么现实意义？
2. **已知条件**：题目给出了哪些数据和参数？
3. **求解目标**：题目要求解什么？有几个问题/子问题？
4. **约束条件**：有哪些必须满足的限制？
5. **评价指标**：如何评判解答的好坏？
6. **核心难点**：你认为这个题目的关键难点在哪里？
7. **涉及领域**：这个问题涉及哪些学科知识（如运筹学、统计学、物理学等）？

请用中文详细分析，对每个子问题分别说明。`,
  {
    label: '提取题目信息',
    phase: '阅读题目',
  }
)

phase('背景调研')
log('正在进行背景知识和相关方法的调研...')

// 第二步：并行搜索背景知识
const [backgroundKnowledge, relatedMethods, similarProblems] = await parallel([
  () => agent(
    `请针对以下数学建模问题，搜索并总结相关的背景知识、行业标准和实际应用：

    ${problemInfo.substring(0, 3000)}

    请重点说明：
    1. 该问题在实际工程/管理中的背景
    2. 行业内常用的解决方案
    3. 相关的政策和标准
    4. 近年的研究热点和发展趋势`,
    {
      label: '背景知识调研',
      phase: '背景调研',
    }
  ),
  () => agent(
    `请针对以下数学建模问题，搜索适用的数学模型和求解方法：

    ${problemInfo.substring(0, 3000)}

    请列出：
    1. 可能适用的数学模型（如优化模型、微分方程、统计模型等）
    2. 每种模型的适用条件和优缺点
    3. 对应的求解算法（精确算法、启发式算法、仿真方法等）
    4. 推荐的工具和软件（Python库、求解器等）`,
    {
      label: '方法调研',
      phase: '背景调研',
    }
  ),
  () => agent(
    `请搜索历年数学建模竞赛中与以下问题相似的题目：

    ${problemInfo.substring(0, 3000)}

    请总结：
    1. 相似题目的年份和题目名称（国赛、美赛等）
    2. 那些题目的优秀论文采用了什么方法
    3. 可借鉴的思路和教训
    4. 本题与相似题目的关键区别`,
    {
      label: '相似题目调研',
      phase: '背景调研',
    }
  ),
])

phase('综合分析')
log('正在整合分析结果...')

// 第三步：综合所有信息生成最终报告
const finalReport = await agent(
  `请根据以下信息，生成一份完整的题目分析报告：

## 题目基本信息
${problemInfo}

## 背景知识
${backgroundKnowledge}

## 适用方法
${relatedMethods}

## 相似题目
${similarProblems}

请按以下结构输出报告：

# 题目分析报告

## 一、题目概要
（1-2段概括题目核心内容）

## 二、问题分解
（将题目拆解为子问题，每个子问题说明输入、输出、关键约束）

## 三、解题路线图
（给出推荐的解题思路，以流程图或步骤形式呈现）

## 四、关键技术点
（列出需要用到的关键数学方法和算法）

## 五、数据需求分析
（分析需要哪些数据，以及数据的特点）

## 六、风险与注意事项
（指出可能的陷阱、容易出错的地方）

## 七、建议时间分配
（给出各阶段的时间分配建议）

请确保报告详细、实用、可操作。`,
  {
    label: '生成分析报告',
    phase: '综合分析',
  }
)

log('题目分析完成！')
return finalReport
