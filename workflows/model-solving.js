export const meta = {
  name: 'model-solving',
  description: '模型求解：根据算法方案生成、调试和运行模型代码',
  phases: [
    { title: '代码生成', detail: '根据算法设计生成完整代码' },
    { title: '测试运行', detail: '运行代码并调试错误' },
    { title: '结果整理', detail: '输出结构化的求解结果' },
  ],
}

const algoPlan = args || '使用默认优化算法'

phase('代码生成')
log('正在根据算法方案生成模型代码...')

const modelCode = await agent(
  `请根据以下算法方案，生成完整的Python模型求解代码：

## 算法方案
${algoPlan}

## 代码要求
1. **模块化结构**：
   - data_loader.py: 数据读取模块
   - model.py: 核心模型定义
   - solver.py: 求解算法实现
   - utils.py: 工具函数（可视化、评估等）
   - main.py: 主入口

2. **代码规范**：
   - 使用类型注解(Type Hints)
   - 完整的docstring文档字符串
   - 详细的注释说明关键步骤
   - 使用logging记录运行日志
   - 设置随机种子确保可复现

3. **错误处理**：
   - 输入数据验证
   - 异常捕获与处理
   - 合理的断言检查

4. **性能优化**：
   - 使用numpy向量化操作
   - 避免不必要的循环
   - 大循环使用tqdm进度条

请直接输出完整的可运行代码。`,
  { label: '生成模型代码', phase: '代码生成' }
)

phase('测试运行')
log('正在测试运行代码...')

const [testResult, debugReport] = await parallel([
  () => agent(
    `请运行以下模型代码（或检查其逻辑正确性），验证：

${modelCode.substring(0, 5000)}

请检查：
1. **语法正确性**：是否有语法错误
2. **导入完整性**：所有依赖是否已导入
3. **逻辑一致性**：算法流程是否完整无遗漏
4. **边界处理**：是否处理了空数据、单元素等特殊情况
5. **数值稳定性**：是否有除零、溢出等风险

输出测试报告，如有错误请给出修复方案。`,
    { label: '测试验证', phase: '测试运行' }
  ),
  () => agent(
    `请对以下代码进行深度代码审查，重点检查数学建模特有的问题：

${modelCode.substring(0, 5000)}

请检查：
1. **数学模型实现**：目标函数和约束条件是否完全正确实现
2. **算法正确性**：算法步骤是否与设计一致
3. **参数设置**：算法参数（学习率、种群大小等）是否合理
4. **收敛性**：迭代终止条件是否合理
5. **输出完整性**：是否输出了所有需要的指标

针对每个问题给出具体的修复代码片段。`,
    { label: '深度调试', phase: '测试运行' }
  ),
])

phase('结果整理')
log('正在整理求解结果...')

const resultsSummary = await agent(
  `请根据以下代码和测试结果，整理一份结构化的求解结果报告：

## 代码
${modelCode.substring(0, 3000)}

## 测试结果
${testResult}

## 调试报告
${debugReport}

请输出：

# 模型求解结果报告

## 一、模型概述
（简要描述建立的数学模型）

## 二、算法实现
（说明算法的具体实现和关键参数）

## 三、求解结果
（主要的数值结果，用表格展示）

## 四、可视化说明
（建议生成哪些图表来展示结果）

## 五、结果初步分析
（对结果的合理性和有效性进行初步分析）

## 六、后续改进建议
（指出可能的改进方向）`,
  { label: '结果整理', phase: '结果整理' }
)

log('模型求解完成！')
return { modelCode, testResult, debugReport, resultsSummary }
