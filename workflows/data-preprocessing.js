export const meta = {
  name: 'data-preprocessing',
  description: '数据预处理：质量检查、清洗、探索性分析与可视化',
  phases: [
    { title: '数据探查', detail: '扫描数据结构与质量' },
    { title: '清洗转换', detail: '缺失值处理与特征工程' },
    { title: '可视化', detail: '生成探索性分析图表' },
  ],
}

const dataPath = args || '数据/'

phase('数据探查')
log(`正在探查数据: ${dataPath}`)

const dataProfile = await agent(
  `请检查数据路径"${dataPath}"下的所有数据文件，生成数据探查报告：

1. **文件清单**：列出所有数据文件名、格式、大小
2. **字段说明**：每个文件包含哪些列/字段，含义是什么
3. **数据类型**：每列的数据类型（数值、分类、文本、时间等）
4. **基本统计**：数值列的均值、中位数、标准差、最大最小值
5. **缺失值**：各列缺失值数量和比例
6. **异常值**：检测可能的异常值（如负值、极大值、不一致值）
7. **关联关系**：文件之间的外键关联关系

请逐个文件分析，输出详细报告。`,
  { label: '数据探查', phase: '数据探查' }
)

phase('清洗转换')
log('正在处理数据质量问题...')

const [preprocessPlan, featureEngineering] = await parallel([
  () => agent(
    `根据以下数据探查结果，设计数据清洗方案并生成Python代码：

${dataProfile.substring(0, 4000)}

请提供：
1. **缺失值处理策略**：对每列的缺失值采用什么填充方法（均值/中位数/众数/插值/删除），说明理由
2. **异常值处理策略**：对检测到的异常值如何处理（剔除/截断/保留），说明理由
3. **数据转换**：是否需要标准化/归一化/对数变换等
4. **数据合并**：多个数据文件如何合并为统一的分析数据

输出可直接运行的Python代码，包含完整的注释。`,
    { label: '数据清洗方案', phase: '清洗转换' }
  ),
  () => agent(
    `根据以下数据探查结果，分析需要构造的特征：

${dataProfile.substring(0, 4000)}

请提供：
1. **派生特征**：从现有字段可以构造哪些新特征
2. **聚合特征**：是否需要按维度聚合
3. **离散化**：连续变量是否需要分段
4. **编码方案**：分类变量采用什么编码方式

输出特征工程方案的Python代码。`,
    { label: '特征工程', phase: '清洗转换' }
  ),
])

phase('可视化')
log('正在生成探索性分析图表...')

const visCode = await agent(
  `根据以下数据信息，生成Python探索性数据分析(EDA)可视化代码：

${dataProfile.substring(0, 4000)}

请生成以下图表的matplotlib/seaborn代码：
1. **分布图**：关键数值变量的直方图和密度图
2. **箱线图**：检测异常值和分布特征
3. **相关性热力图**：数值变量之间的相关性
4. **散点图矩阵**：重要变量之间的散点关系
5. **时间序列图**：如果有时间维度
6. **地理分布图**：如果有坐标数据

输出完整可运行的Python代码，使用中文字体，图表分辨率为300dpi。`,
  { label: '生成可视化代码', phase: '可视化' }
)

log('数据预处理完成！')
return { dataProfile, preprocessPlan, featureEngineering, visCode }
